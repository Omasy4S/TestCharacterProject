using TestCharacterProject.Models;

namespace TestCharacterProject.Weapons
{
    /// <summary>
    /// Интерфейс для всех видов оружия в игре.
    /// Используем интерфейс, а не абстрактный класс, потому что:
    /// 1. Оружие может иметь разную логику расчета урона без общей базовой реализации
    /// 2. В будущем можно добавить множественное наследование поведений
    /// 3. Легче тестировать и мокировать
    /// </summary>
    public interface IWeapon
    {
        /// <summary>
        /// Имя оружия для отображения в интерфейсе.
        /// </summary>
        string Name { get; }

        /// <summary>
        /// Рассчитывает урон на основе характеристик атакующего.
        /// ВАЖНО: Метод может изменять характеристики атакующего (побочный эффект)!
        /// Например, FoolStaff меняет Strength и Magic местами у магов.
        /// </summary>
        /// <param name="attacker">Персонаж, который атакует</param>
        /// <returns>Количество урона, которое будет нанесено цели</returns>
        int CalculateDamage(Character attacker);
    }
}
